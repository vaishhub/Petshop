      <center>  <b >Heartworm Disease</b> </center><br>
        <p>   Heartworm disease in dogs is a blood-borne parasitic nematode (roundworm) known as Dirofilaria immitis transmitted by mosquitoes.
			 Upwards of 30 species of mosquitoes can act as heartworm transmitters. 
			 Mosquitoes ingest immature heartworm larvae, called microfilariae, by feeding on either an infected cat or dog.
			  The microfilariae develop further for 10 to 30 days in the mosquito's gut and then enter parts of the mosquito's mouth.
			   When an infected mosquito bites a dog, it injects larvae into the dog.
			   The larvae then mature over a period of several months, eventually ending up in the right side of the heart and the pulmonary arteries.
			    Once this occurs, they mature into adult heartworms in dogs, and can reproduce about six months from the time of invasion.
			    At approximately eight months after the invasion, heartworm in dogs begin to produce a new crop of microfilariae that will live in the dog's blood for about one month. 
				By the time this occurs, their lives are in danger even though clinical signs may not be apparent yet.
        Symptoms of Heartworm Disease ?
				<p>If your dog is affected with Heartworm Disease, you may notice one or more of the following symptoms:</p>
             <ul>
<li> <p> The most obvious clinical signs of heartworm disease are a soft</p></li>
<li><p>  dry cough  </p></li>
<li><p>shortness of breath</p></li>
<li><p>weakness</p></li>
<li><p>listlessness and loss of stamina</P></li>
<li><p>weakness</p></li>
            </ul>
            <b style="font-size: 20px ; font-family:Arial, Helvetica, sans-serif" > How Is Heartworm Disease Treated? </b>
<p>The first step in heartworm treatments is obtaining a diagnosis. Most veterinarians use a battery of tests to determine the presence of heartworms in dogs. The first step in diagnosis of heartworm in dogs is to perform a blood test.
A urinalysis, or the testing of a dog's urineAn antigen test determines the presence of adult female heartworms Radiographs, or X-Rays to view the size and shape of a dog's heart. This is helpful because many dogs with heartworm develop enlarged pulmonary arteries, or have obstructions in the arteries leading to the lungs
An ultrasound allows us to directly view the internal structures of the heart and surrounding vessels, in order to assess the condition and function of the heart
During initial heartworm treatments, most patients are hospitalized to receive an adulticide, which is a medication that kills adult heartworms. 
The microfilariae in the body, or baby heartworms, can be eliminated with a monthly prevention, which can be administered at home.
For more severe cases, such as dogs experiencing thromboembolic complications (in which a blood clot that has formed breaks loose and travels through the bloodstream to clot another vessel), hospitalization may be necessary for a longer period of time while heartworm treatments are administered.
 In some extreme cases, a surgical procedure may be necessary to remove adult worms from the right heart and pulmonary artery by way of the jugular vein.
 This procedure is recommended if the infestation consists of a high number of adult worms.</p>
