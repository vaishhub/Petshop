      <center>  <b >Canine Parvovirus</center></b><br>


        <p>Canine Parvovirus<br>
          Canine parvovirus (CPV) is a highly contagious viral disease of dogs that commonly causes acute gastrointestinal illness in puppies. 
          The disease most often strikes in pups between six and 20 weeks old, but older animals are sometimes also affected.
           A rare variant of the disease may be seen in very young (neonatal) puppies is myocarditis (an inflammation of the heart muscle).</p>
           <b style="font-size: 20px ; font-family:Arial, Helvetica, sans-serif" > Symptoms of Canine Parvovirus ?</b>
           <p>Symptoms often associated with CPV include lethargy, depression, and loss or lack of appetite, followed by a sudden onset of high fever, vomiting, and diarrhea.
             If your dog is experiencing bouts of bloody diarrhea and/or vomiting, CPV is only one of several potential culprits.
             Your veterinarian can run several tests to help determine whether your dog is infected with CPV.</p>
             <b style="font-size: 20px ; font-family:Arial, Helvetica, sans-serif"> How Is Heartworm Disease Treated? </b>
             <b style="font-size: 20px ; font-family:Arial, Helvetica, sans-serif" >What are the treatment options for dogs with CPV?</b>
              Treatment options for dogs suffering from CPV involve supportive care and management of symptoms. 
              Treatment options will vary, depending on how sick the dog is, but certain aspects are considered vital for all patients.
               A hospital stay is often necessary so that the dog can receive intravenous fluids and nutrients to replace the vast quantities lost via vomiting and diarrhea. 
              An intravenous drip is preferred because the digestive tract of stricken dogs is usually in distress and can’t tolerate or absorb what the dog needs.
               Blood transfusions may also be helpful to boost low blood cell counts that may result from CPV infecting the bone marrow.Antibiotics may be appropriate therapy for a dog suffering from CPV,
                administered either intravenously or as injections, to help fight the infection if intestinal bacteria have entered the bloodstream.
               In addition, medications to control nausea and diarrhea are sometimes useful.
               Many dogs will respond to medical therapy if it is initiated in a timely fashion, and those dogs that recover from CPV infection retain lifelong protective immunity against the strain that infected them.</p>
