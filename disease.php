      <section id="Canine Distemper">
      <center>  <b > Canine Distemper</center></b><br>
        <p>Distemper is a viral disease that attacks the respiratory, gastrointestinal, and nervous systems of dogs. 
          It is spread through airborne droplets or contact with infected animals.
           Symptoms include fever, coughing, vomiting, and seizures.
           Vaccination is the best way to prevent distemper.</p>
           <h3>Symptoms of Canine Distemper </h3>
           <p> Signs and symptoms of canine distemper include:

            Fever
            Nasal discharge
            Eye discharge
            Lethargy
            Sneezing
            Coughing
            Difficulty breathing
            Vomiting
            Diarrhea
            Loss of appetite
            Thickening of nose and foot pads
            Pneumonia 
            Skin sores
            Pain
            Canine distemper may also cause brain inflammation and neurological symptoms, which is often confused with rabies. Neurological symptoms of canine distemper include:
            
            Muscle twitching
            Chewing-gum fits
            Excessive saliva
            Head tilt
            Circling
            Involuntary eye movements
            Paralysis or partial paralysis
            Seizures
            At first, you may not notice symptoms of canine distemper, and it can be mistaken for other viruses and infections.
             Your dog can get a mild or severe case and may have symptoms that only last 10 days.
             However, neurological symptoms may be delayed and show up months after infection.</p>
           <h3>How to Treat Canine Distemper</h3>
           <p> There is no cure for canine distemper.
             Veterinarians diagnose distemper through a combination of clinical signs and diagnostic tests, or through a postmortem necropsy.
             Once diagnosed, care is purely supportive. 
            Veterinarians treat the diarrhea, vomiting, and neurological symptoms, prevent dehydration, and try to prevent secondary infections.
             Most vets recommend that dogs be hospitalized and separated from other dogs to prevent the spread of infection.
             The survival rate and length of infection depend on the strain of the virus and on the strength of the dog’s immune system.
              Some cases resolve as quickly as 10 days.
             Other cases may exhibit neurological symptoms for weeks and even months afterward.</p>

